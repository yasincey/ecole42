#include "ft_printf.h"

int	ft_is_argument(char c)
{
	if (c == 'c' || c == 's' || c == 'd' || c == 'i')
		return (1);
	else if (c == 'x' || c == 'X' || c == 'u' || c == 'p' || c == '%')
		return (1);
	else
		return (0);
}
