#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	i;

	i = count * size;
	ptr = (void *)malloc(i);
	if (!ptr)
		return (NULL);
	else
		ft_bzero(ptr, i);
	return (ptr);
}
